package brawlcardgame;

/**
 * Represents a character from the brawl card game. Each deck has a character it represents,
 * which determines which cards are in the deck and uniquely identifies the deck within a
 * player's collection
 *
 * @author Nicolas Artman
 */
public enum BrawlCharacter {
   // First set
   Bennett, Chris, Darwin, Hale, Morgan, Pearl,
   // Second set
   Alex, Crane, Gina, Mischo, Rent, Tess,
   // Third set
   Nickie, Sonia, Tamiya,
   // Custom decks
   Custom1, Custom2, Custom3, Custom4, Custom5, Custom6, Custom7, Custom8, Custom9, Custom10;
}
